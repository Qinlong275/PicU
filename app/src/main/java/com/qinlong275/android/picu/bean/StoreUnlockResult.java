package com.qinlong275.android.picu.bean;

public class StoreUnlockResult {
    private String uid;
    private String picu_id;
    private String successed;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getPicu_id() {
        return picu_id;
    }

    public void setPicu_id(String picu_id) {
        this.picu_id = picu_id;
    }

    public String getSuccessed() {
        return successed;
    }

    public void setSuccessed(String successed) {
        this.successed = successed;
    }
}
